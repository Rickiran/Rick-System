    private void jb_sign_userActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_sign_userActionPerformed
    int count=0;
    SimpleDateFormat formatter = new SimpleDateFormat("dd/MM/yyyy HH:mm:ss");  
    Date date = new Date();   
    if(!(jt_name.getText().trim()).equals("")){
        jt_name.setBackground(Color.green);
    }
    else{
        jt_name.setBackground(Color.red);  
        count++;
    }
    if(!(jt_email.getText().trim()).equals("")){
        jt_email.setBackground(Color.green);
    }
    else{
        jt_email.setBackground(Color.red);  
        count++;
    }
    if(!(jt_telephone.getText().trim()).equals("")){
        jt_telephone.setBackground(Color.green);
    }
    else{
        jt_telephone.setBackground(Color.red);  
        count++;
    }
    if(!(jt_address.getText().trim()).equals("")){
        jt_address.setBackground(Color.green);
    }
    else{
        jt_address.setBackground(Color.red);  
        count++;
    }
    
    if(count==0){
        try{
         Connection cn=Connect.Cnnct();
         PreparedStatement ps=cn.prepareStatement("insert into clientes values (?,?,?,?,?,?)");
         ps.setInt(1, 0);
         ps.setString(2, jt_name.getText().trim());
         ps.setString(3,jt_email.getText().trim());
         ps.setString(4, jt_telephone.getText().trim());
         ps.setString(5, jt_address.getText().trim());
         ps.setString(6, formatter.format(date));
         ps.executeUpdate();
         cn.close();
         JOptionPane.showMessageDialog(null, "Fields charge was successfull complete");
         this.dispose();
     }
     catch(SQLException e){
         JOptionPane.showMessageDialog(null, "There is a problem to update the fields");
     }
        
    }
    else{
        JOptionPane.showMessageDialog(null,"There are fields to fill out please complete the field(s)");
    }
    }//GEN-LAST:event_jb_sign_userActionPerformed

