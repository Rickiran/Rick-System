    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int ID;
        String cDev="";
        String Mark="";
        ID=LOG_USER.ID;
        int count=0;
        if((jc_dev.getSelectedIndex()+1)==1 ){
            cDev="Laptop";
        }else if((jc_dev.getSelectedIndex()+1)==2){
            cDev="Desktop";
        }else if((jc_dev.getSelectedIndex()+1)==3){
            cDev="Printer";
        }
        else if((jc_dev.getSelectedIndex()+1)==4){
            cDev="MultiFunctional";
        }
        
        if((jc_mark.getSelectedIndex()+1)==1){
            Mark="Acer";
        }else if((jc_mark.getSelectedIndex()+1)==2){
            Mark="Allenware";
        }else if((jc_mark.getSelectedIndex()+1)==3){
            Mark="Apple";
        }else if((jc_mark.getSelectedIndex()+1)==4){
            Mark="Asus";
        }else if((jc_mark.getSelectedIndex()+1)==5){
            Mark="Brother";
        }else if((jc_mark.getSelectedIndex()+1)==6){
            Mark="Dell";
        }else if((jc_mark.getSelectedIndex()+1)==7){
            Mark="HP";
        }else if((jc_mark.getSelectedIndex()+1)==8){
            Mark="Lenovo";
        }
        
        SimpleDateFormat sdf=new SimpleDateFormat("dd/MM/yyyy HH:mm:ss");
        SimpleDateFormat day=new SimpleDateFormat("dd");
        SimpleDateFormat month=new SimpleDateFormat("MM");
        SimpleDateFormat year=new SimpleDateFormat("yyyy");
        Date dnow =new Date();
       
        if((jt_name.getText().trim()).equals("")){
            jt_name.setBackground(red);
            count++;
        }
        if((jt_model.getText().trim()).equals("")){
            jt_model.setBackground(red);
            count++;
        }
        if((jt_serie.getText().trim()).equals("")){
            jt_serie.setBackground(red);
            count++;
        }
        if(count==0){
        try{
            Connection cn=Connect.Cnnct();
            PreparedStatement ps=cn.prepareStatement("insert into equipos values (?,?,?,?,?,?,?,?,?,?,?,?,?,?)");
            ps.setInt(1,0);
            ps.setInt(2, ID);
            ps.setString(3, cDev);
            ps.setString(4, Mark);
            ps.setString(5,jt_model.getText().trim());
            ps.setString(6,jt_serie.getText().trim());
            ps.setString(7,(String)day.format(dnow));
            ps.setString(8,(String)month.format(dnow));
            ps.setString(9,(String)year.format(dnow));
            ps.setString(10,jt_comments.getText().trim());
            ps.setString(11,"Active");
            ps.setString(12,(String)sdf.format(dnow));
            ps.setString(13,jt_comments.getText().trim());
            ps.setString(14,user);
            ps.executeUpdate();
            cn.close();
            JOptionPane.showMessageDialog(null,"Success Update!");
            this.dispose();
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null,"There is an error to fill the customer name out");
        }
        }
        else{
            JOptionPane.showMessageDialog(null,"Please complete all the fields");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

